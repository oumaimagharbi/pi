package tn.esprit.spring.domain.util;

public class StatisticCountMsg {

	private long count;

	public StatisticCountMsg(long count) {
		super();
		this.count = count;
	}

	public StatisticCountMsg() {
		super();
	}

	public long getCount() {
		return count;
	}

	public void setCount(long count) {
		this.count = count;
	}
	
	
}
