package tn.esprit.spring.domain.util;

public class StatisticUser {

private long count;
	







	public StatisticUser() {
	super();
}

	public StatisticUser(long count) {
	super();
	this.count = count;
}

	public long getCount() {
		return count;
	}

	public void setCount(long count) {
		this.count = count;
	}
	
}
